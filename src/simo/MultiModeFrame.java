	private void jRadioButton_scaleFreeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton_scaleFreeActionPerformed
		if (this.jRadioButton_scaleFree.isSelected()) {
			this.jTextField_degree.setEnabled(true);
			this.jTextField_avgDegree.setEnabled(false);
			this.jRadioButton_randomAvgDegree.setSelected(false);
			this.jRadioButton_random.setSelected(false);
		} else {
			this.jTextField_degree.setEnabled(false);
			this.jTextField_avgDegree.setEnabled(true);
			this.jRadioButton_randomAvgDegree.setSelected(true);
			this.jRadioButton_random.setSelected(false);
		}
	}//GEN-LAST:event_jRadioButton_scaleFreeActionPerformed

	private void jRadioButton_randomAvgDegreeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton_randomAvgDegreeActionPerformed
		if (this.jRadioButton_randomAvgDegree.isSelected()) {
			this.jTextField_avgDegree.setEnabled(true);
			this.jTextField_degree.setEnabled(false);
			this.jRadioButton_random.setSelected(false);
			this.jRadioButton_scaleFree.setSelected(false);
		} else {
			this.jTextField_avgDegree.setEnabled(false);
			this.jTextField_degree.setEnabled(false);
			this.jRadioButton_random.setSelected(true);
			this.jRadioButton_scaleFree.setSelected(false);
		}
	}//GEN-LAST:event_jRadioButton_randomAvgDegreeActionPerformed

	private void jRadioButton_randomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton_randomActionPerformed
		if (this.jRadioButton_random.isSelected()) {
			this.jTextField_avgDegree.setEnabled(false);
			this.jTextField_degree.setEnabled(false);
			this.jRadioButton_randomAvgDegree.setSelected(false);
			this.jRadioButton_scaleFree.setSelected(false);
		} else {
			this.jTextField_avgDegree.setEnabled(false);
			this.jTextField_degree.setEnabled(true);
			this.jRadioButton_scaleFree.setSelected(true);
			this.jRadioButton_randomAvgDegree.setSelected(false);
		}
	}//GEN-LAST:event_jRadioButton_randomActionPerformed

