	private void jButton_simulateActionPerformed(java.awt.event.ActionEvent evt) {
		//GEN-FIRST:event_jButton_simulateActionPerformed
		this.setVisible(false);
		JFrame jframe = new JFrame("Running...");
		jframe.setSize(300, 100);
		jframe.setVisible(true);
		jframe.setDefaultCloseOperation(EXIT_ON_CLOSE);
		jframe.add(new JLabel("Close this window to terminate the program!"));
		jframe.setLocationRelativeTo(null);
		int minNodes = Integer.parseInt(this.jTextField_min.getText());
		int step = Integer.parseInt(this.jTextField_step.getText());
		int maxNodes = Integer.parseInt(this.jTextField_max.getText());
		int numOfTests = Integer.parseInt(this.jTextField_test.getText());
		int searches = 0;
		boolean absolute = true;
		boolean batch = false;
		int batchSize = 0;
		if (this.jRadioButton_absolute.isSelected()) {
			searches = Integer.parseInt(this.jTextField_absolute.getText());
		}
		if (this.jRadioButton_relative.isSelected()) {
			absolute = false;
			searches = Integer.parseInt(this.jTextField_relative.getText());
		}
		if (this.jRadioButton_batch.isSelected()) {
			batch = true;
			batchSize = Integer.parseInt(this.jTextField_batch.getText());
		}
		int degree = Integer.parseInt(this.jTextField_degree.getText());
		GraphType graphType = GraphType.ScaleFree;
		if (this.jRadioButton_randomAvgDegree.isSelected()) {
			degree = Integer.parseInt(this.jTextField_avgDegree.getText());
			graphType = GraphType.RandomAvgDegree;
		}
		if (this.jRadioButton_random.isSelected()) {
			graphType = GraphType.Random;
		}
		Protocol protocol = Protocol.Naive;
		if (this.jRadioButton_alternative.isSelected()) {
			protocol = Protocol.Alternative;
		}
		if (this.jRadioButton_both.isSelected()) {
			protocol = Protocol.Both;
		}
		MsController.getInstance().setProperties(protocol, minNodes, searches, graphType, degree, numOfTests, true, false);
		if (!batch) {
			MsController.getInstance().simulateMulti(protocol, minNodes, step, maxNodes, absolute, searches, MultiModeFrame.this);
		} else {
			MsController.getInstance().simulateMultiToStable(protocol, minNodes, step, maxNodes, batchSize, MultiModeFrame.this);
		}
	}//GEN-LAST:event_jButton_simulateActionPerformed

